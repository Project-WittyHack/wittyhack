    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        String chosenAnswer = getUserAnswer();
        if(chosenAnswer != null)
        {
            Questions question = qstore.getQuestion(pos);
            String correctAnswer = question.getCorrectAnswer();
            Answer newanswer = new Answer(newExam.getExamid(),newExam.getLanguage() , qno, chosenAnswer, correctAnswer);
            Answer answer = astore.getAnswerByQno(qno);
            if(answer == null)
            {
                astore.addAnswer(newanswer);
            }
            else
            {
                if(newanswer.getChosenAnswer().equals(answer.getCorrectAnswer()) == false)
                {
                    int apos = astore.removeAnswer(answer);
                    astore.setAnswerAt(apos, newanswer);
                }
            }
        }
            pos++;
            if(pos >= qstore.getCount())
                pos = 0;
            qno++;
            if(qno > qstore.getCount())
            {
                qno = 1;
                JOptionPane.showMessageDialog(null,"This is final question of the test. \n Click on DONE button to submit the test.", "Submit!!", JOptionPane.INFORMATION_MESSAGE);
            }
            showQuestions();
            jlbQno.setText("Question No "+qno);
            System.out.println(pos+"yaha");
        
    }//GEN-LAST:event_btnNextActionPerformed

