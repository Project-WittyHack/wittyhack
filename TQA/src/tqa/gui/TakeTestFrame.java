    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        String chosenAnswer = getUserAnswer();
        if(chosenAnswer != null)
        {
            Questions question = qstore.getQuestion(pos);
            String correctAnswer = question.getCorrectAnswer();
            Answer newanswer = new Answer(newExam.getExamid(),newExam.getLanguage() , qno, chosenAnswer, correctAnswer);
            Answer answer = astore.getAnswerByQno(qno);
            if(answer == null)
            {
                astore.addAnswer(newanswer);
            }
            else
            {
                if(newanswer.getChosenAnswer().equals(answer.getCorrectAnswer()) == false)
                {
                    int apos = astore.removeAnswer(answer);
                    astore.setAnswerAt(apos, newanswer);
                }
            }
        }
        int right = 0, wrong = 0;
        for(Questions que : qstore.getAllQuestion())
        {
            int qno = que.getQno();
            
            Answer answer = astore.getAnswerByQno(qno);
            if(answer == null)
                continue;
            else
            {
                String chosenAnswer1 = answer.getChosenAnswer();
                String correctAnswer = answer.getCorrectAnswer();
                if(chosenAnswer1.equals(correctAnswer))
                    ++right;
                else
                    ++wrong;
            }
        }
        String reportCard = "Total Questions : "+qstore.getCount();
        reportCard += "\n Right Answers : "+right;
        reportCard +="\n Wrong Answes : "+wrong;
        reportCard +="\n UnAttempted Answes : "+(qstore.getCount()-(right + wrong));
        JOptionPane.showMessageDialog(null,reportCard,"Here Is Your Result",JOptionPane.INFORMATION_MESSAGE);
        try
        {
            Performance performance = new Performance(UserProfile.getUsername(),newExam.getExamid(), right, wrong, (qstore.getCount()-(right + wrong)),(double)right/qstore.getCount()*100, newExam.getLanguage());
            PerformanceDAO.addPerformance(performance);
            JOptionPane.showMessageDialog(null,"Your Performance has been successfully added to the database","Performance Added",JOptionPane.INFORMATION_MESSAGE);
            PaperSelectionFrame pf = new PaperSelectionFrame();
            pf.setVisible(true);
            this.dispose();
        }
        catch(SQLException sq)
        {
            JOptionPane.showMessageDialog(null, ""+sq, "ERROR!!",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDoneActionPerformed

