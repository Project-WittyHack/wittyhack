    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if(validateInputs()==0)
        {
            JOptionPane.showMessageDialog(null,"Please fill all the fields", "Incomplete", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
           Users e = new Users();
           if(validateInputs()==-1)
               JOptionPane.showMessageDialog(null, "Password Too Weak","Length less than 4 character",JOptionPane.ERROR_MESSAGE);
           else if(validateInputs()==-2)
               JOptionPane.showMessageDialog(null,"Password and ReTyprepassword are not matching","Invalid passwords", JOptionPane.ERROR_MESSAGE);
           else
           {
               e.setUserId(studuserid);
               e.setPassword(studpassword);
               e.setUserType("student");
               boolean b = UsersDAO.addUser(e);
               if(b)
               {
                   JOptionPane.showMessageDialog(null,"User Succeessfully Registered", "SUCCESS",JOptionPane.INFORMATION_MESSAGE);
                   AdminOptionsFrame ad = new AdminOptionsFrame();
                   ad.setVisible(true);
                   this.dispose();
               }
               else
                   JOptionPane.showMessageDialog(null,"User Already Exists","Error in Registering", JOptionPane.ERROR_MESSAGE);
           }
           
        }
        catch(SQLException sq)
        {
            JOptionPane.showMessageDialog(null, "Error in registering"+sq,"ERROR!!",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

