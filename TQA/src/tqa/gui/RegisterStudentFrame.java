    private int validateInputs()
    {
        studuserid = txtUserId.getText().trim();
        char[] pwd = txtPassword.getPassword();
        studpassword = String.valueOf(pwd);
        char[]rpwd = txtReTypePassword.getPassword();
        studretypepassword = String.valueOf(rpwd);
        if(studuserid.isEmpty() || pwd.length == 0 || rpwd.length == 0)
            return 0;
        else if(studuserid.length() <= 4 || studpassword.length() <= 4)
        {
            studpassword = String.valueOf(pwd);
            studretypepassword = String.valueOf(rpwd);
            return -1;
        }
        else if(studpassword.equals(studretypepassword) == false)
        {
            return -2; 
        }
        else
            return 1;
    }
