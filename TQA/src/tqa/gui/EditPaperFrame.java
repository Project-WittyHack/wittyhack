    private void jcSubjectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcSubjectActionPerformed
        if(validateInputs()==false)
        {
            JOptionPane.showMessageDialog(null, "Please Choose A Subject","Selection Not Made", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try
        {
            ArrayList<String> examList = ExamDAO.getExamIdBySubject(subjectname);
            if(examList.isEmpty())
            {
                    JOptionPane.showMessageDialog(null, "No Paper Set for This Subject", "Paper Not Found!!", JOptionPane.ERROR_MESSAGE);
                    return;
            }
            jcExamId.removeAllItems();
            for(String e: examList)
            {
                jcExamId.addItem(e);
            }
        }
        catch(SQLException sq)
        {
            JOptionPane.showMessageDialog(null, "Error in retreiving the examid","ERROR!!", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jcSubjectActionPerformed

