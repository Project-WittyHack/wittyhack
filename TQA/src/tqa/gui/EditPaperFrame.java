    private void btnEditTheQuestionsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditTheQuestionsActionPerformed
        if(jcSubject.getSelectedIndex() == 0)
        {
            JOptionPane.showMessageDialog(null,"Please make a selection", "ERROR",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String subject = jcSubject.getSelectedItem().toString();
        String examid = jcExamId.getSelectedItem().toString();
        int ans;
        ans = JOptionPane.showConfirmDialog(null,"You have chosen \t"+subject+"\t and \t"+examid+"\t paper. \n Is This OK?", "Confirmation!", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(ans == JOptionPane.YES_OPTION)
        {
            try
            {
                int totalquestion = ExamDAO.getQuestionCountByExamId(examid.trim());
                Exam editExam = new Exam(examid,subject,totalquestion);
                EditQuestionsFrame eq = new EditQuestionsFrame(editExam);
                eq.setVisible(true);
                this.dispose();
            }
            catch(SQLException sq)
            {
                JOptionPane.showMessageDialog(null,"Error in fetching the questions"+sq,"ERROR!!",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEditTheQuestionsActionPerformed

